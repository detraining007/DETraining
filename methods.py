def add(num1,num2):
        sum = num1 + num2
        # print(f'sum of numbers {number1} and {number2} is {sum}')
        return sum
def sub(num1,num2):
        subt = num1-num2
        return subt
def mul(num1,num2):
        product = num1*num2
        return product
def div(num1,num2):
        divison = num1/num2
        return divison

