def add(Num_1,Num_2):
  return Num_1+Num_2
def sub(Num_1,Num_2):
  return Num_1-Num_2
def Multi(Num_1,Num_2):
  return Num_1*Num_2
def Div(Num_1,Num_2):
  if Num_2 ==0:
    return "Divison with 0 is undefined"
  return Num_1//Num_2
def Remainder(Num_1,Num_2):
  if Num_2 ==0:
    return "Remainder with 0 is undefined"
  return Num_1%Num_2


